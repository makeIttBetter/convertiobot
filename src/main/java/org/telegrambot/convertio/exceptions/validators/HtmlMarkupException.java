package org.telegrambot.convertio.exceptions.validators;

public class HtmlMarkupException extends ValidationException {
    public HtmlMarkupException() {
        super();
    }

    public HtmlMarkupException(String message) {
        super(message);
    }

    public HtmlMarkupException(String message, Throwable cause) {
        super(message, cause);
    }

    public HtmlMarkupException(Throwable cause) {
        super(cause);
    }

}
