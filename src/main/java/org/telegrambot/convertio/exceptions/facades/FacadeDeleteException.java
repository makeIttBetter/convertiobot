package org.telegrambot.convertio.exceptions.facades;

public class FacadeDeleteException extends FacadeException {
    public FacadeDeleteException() {
        super();
    }

    public FacadeDeleteException(String message) {
        super(message);
    }

    public FacadeDeleteException(String message, Throwable cause) {
        super(message, cause);
    }

    public FacadeDeleteException(Throwable cause) {
        super(cause);
    }
}
